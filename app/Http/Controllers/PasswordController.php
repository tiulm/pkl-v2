    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $this->validate(
            $request,
            [
                'current-password' => 'required|min:8',
                'new-password' => 'required|different:current-password|min:8',
                'new-password_confirmation' => 'required|same:new-password|min:8',
            ],
            [
                'required' => 'Harap di isi',
                'different' => 'Password baru harus berbeda dari password sebelumnya',
                'same' => 'Harus sama dengan Password baru',
                'min' => 'Minimal :min karakter'
            ]
        );

        if (!(Hash::check($request->get('current-password'), Auth::user()->password))) {
            // The passwords matches
            return redirect()->back()->with("error", "Your current password does not matches with the password you provided. Please try again.");
        }

        if (strcmp($request->get('current-password'), $request->get('new-password')) == 0) {
            //Current password and new password are same
            return redirect()->back()->with("error", "New Password cannot be same as your current password. Please choose a different password.");
        }

        $validatedData = $request->validate([
            'current-password' => 'required',
            'new-password' => 'required|string|min:6|confirmed',
        ]);

        //Change Password
        $user = Auth::user();
        $user->password = $request->get('new-password');
        $user->save();

        return redirect()->back()->with("success", "Password changed successfully !");
    }

